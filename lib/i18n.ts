export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function getModuleMeta(locale: Locale) {
  const dictionary = getDictionary(locale);

  return {
    RECIPES: {
      href: "/recipes",
      label: dictionary.modules.RECIPES.label,
      summary: dictionary.modules.RECIPES.summary,
    },
    IMPORT: {
      href: "/import",
      label: dictionary.modules.IMPORT.label,
      summary: dictionary.modules.IMPORT.summary,
    },
    EXPORT: {
      href: "/export",
      label: dictionary.modules.EXPORT.label,
      summary: dictionary.modules.EXPORT.summary,
    },
  } satisfies Record<ModuleKey, { href: string; label: string; summary: string }>;
}

export type Dictionary = ReturnType<typeof getDictionary>;
